// 실수 자료형 실습(double, float 비교)
public class DoubleEx2 {
    public static void main(String[] args) {
        // float 타입 실수
        float a = 3.14f;
        float b = 1.5f;

        System.out.println("float a: " + a);  // 출력: 3.14
        System.out.println("float b: " + b);  // 출력: 1.5

        // double 타입 실수
        double x = 0.1;
        double y = 0.23456;

        System.out.println("double x: " + x);   // 출력: 0.1
        System.out.println("double y: " + y);   // 출력: 0.23456
    }
}
