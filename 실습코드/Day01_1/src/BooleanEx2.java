// Boolean 자료형 실습
public class BooleanEx2 {
    public static void main(String[] args) {
        // boolean 자료형 변수
        boolean isRaining = true;  // 비가 오면 true
        boolean isSunny = false;   // 맑지 않으면 false

        // 결과 출력
        System.out.println("비가 오나요? " + isRaining);  // 출력: 비가 오나요? true
        System.out.println("맑은 날씨인가요? " + isSunny);  // 출력: 맑은 날씨인가요? false
    }
}
