// 변수 선언 및 출력 연습 예제
public class Ex01 {
    public static void main(String[] args) {
        // 변수 선언 및 초기화
        int age = 25;
        double height = 175.5;
        char gender = 'M';

        // 출력
        System.out.println("변수 값:");
        System.out.println("나이: " + age);
        System.out.println("키: " + height);
        System.out.println("성별: " + gender);
    }
}
