// 배열 선언 및 초기화와 배열 요소 출력 연습 예제
public class Ex15 {
    public static void main(String[] args) {
        // 정수형 배열 선언과 동시에 값 할당
        int[] numbers = {10, 20, 30, 40, 50};

        // 배열 요소 출력
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
